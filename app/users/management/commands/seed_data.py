        comments_created = 0

        for group in groups:
            # 3-8 постов на группу
            num_posts = random.randint(3, 8)

            for _ in range(num_posts):
                author = random.choice(all_users)
                week = random.randint(4, 42)
                tip = random.choice(TIPS)
                problem = random.choice(PROBLEMS)
                topic = group.title

