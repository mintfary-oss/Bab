    def _create_groups(self):
        """Создание 300 групп."""
        from chat.models import Group, GroupMember

        existing = Group.objects.count()
        if existing >= 300:
            self.stdout.write(f"Группы уже есть: {existing}. Пропускаем.")
            return

        self.stdout.write("Создаём 300 групп...")

        all_users = list(User.objects.filter(username__startswith="user_")[:10000])
        if not all_users:
            self.stdout.write(self.style.ERROR("Нет пользователей!"))
            return

        groups_created = 0
        topics = GROUP_TOPICS[:300]

        # Если тем меньше 300, дублируем с номерами
        while len(topics) < 300:
            idx = len(topics) - len(GROUP_TOPICS)
            orig = GROUP_TOPICS[idx % len(GROUP_TOPICS)]
            topics.append((f"{orig[0]} #{len(topics) + 1}", orig[1]))

        for i, (title, description) in enumerate(topics):
            owner = random.choice(all_users)
            group = Group.objects.create(
                owner=owner,
                title=title,
                description=description,
                privacy="public",
            )

            # Добавляем 30-100 участников в каждую группу
            members_count = random.randint(30, 100)
            members = random.sample(all_users, min(members_count, len(all_users)))

            member_objects = [
                GroupMember(group=group, user=owner, role="admin")
            ]
            for member in members:
                if member.pk != owner.pk:
                    member_objects.append(
                        GroupMember(group=group, user=member, role="member")
                    )

            GroupMember.objects.bulk_create(member_objects, ignore_conflicts=True)
            groups_created += 1

            if groups_created % 50 == 0:
                self.stdout.write(f"  Создано {groups_created}/300 групп...")

        self.stdout.write(self.style.SUCCESS(f"  Групп: {Group.objects.count()}"))

    def _create_posts_and_comments(self):
        """Создание постов в группах с комментариями."""
        from posts.models import Comment, Post

        existing_posts = Post.objects.count()
        if existing_posts >= 1000:
            self.stdout.write(f"Посты уже есть: {existing_posts}. Пропускаем.")
