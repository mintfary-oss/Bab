    def _create_likes(self):
        """450 лайков на посты каждой группы."""
        from chat.models import Group
        from posts.models import Like, Post

        existing_likes = Like.objects.count()
        if existing_likes >= 10000:
            self.stdout.write(f"Лайки уже есть: {existing_likes}. Пропускаем.")
            return

        self.stdout.write("Создаём лайки (450 на группу)...")

        all_users = list(User.objects.filter(username__startswith="user_")[:10000])
        groups = list(Group.objects.all()[:300])

        # Получаем посты по заголовкам групп
        total_likes = 0

        for group in groups:
            posts = list(
                Post.objects.filter(title__startswith=group.title[:50])[:10]
            )
            if not posts:
                continue

            # 450 лайков распределяем по постам группы
            likes_per_group = 450
            likers = random.sample(all_users, min(likes_per_group, len(all_users)))
            like_objects = []

            for liker in likers:
                post = random.choice(posts)
                like_objects.append(
                    Like(post=post, user=liker)
                )

            # bulk_create с ignore_conflicts чтобы не дублировать
            Like.objects.bulk_create(like_objects, ignore_conflicts=True)
            total_likes += len(like_objects)

            if total_likes % 5000 == 0:
                self.stdout.write(f"  Лайков: {total_likes}...")

        self.stdout.write(self.style.SUCCESS(f"  Всего лайков: {Like.objects.count()}"))
